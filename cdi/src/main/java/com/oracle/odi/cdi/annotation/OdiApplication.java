package com.oracle.odi.cdi.annotation;

import java.lang.annotation.Documented;
import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

import jakarta.inject.Singleton;

/**
 * Annotation that can be added to the application entry point to trigger processing of the
 * CDI build time extensions and beans to import.
 */
@Documented
@Retention(RetentionPolicy.RUNTIME)
@Target(ElementType.TYPE)
@Singleton
public @interface OdiApplication {
}
